# Создаем исходный список
arr = [5, 2, 9, 1, 5, 6]

# Применяем метод sort()
print("Исходный список до sort():", arr)
arr.sort()  # Метод sort() изменяет сам список на месте
print("Список после sort():", arr)  # Вывод: [1, 2, 5, 5, 6, 9]

# Создаем новый исходный список для следующего примера
arr2 = [5, 2, 9, 1, 5, 6]

# Применяем функцию sorted()
print("Исходный список до sorted():", arr2)
sorted_arr = sorted(arr2)  # Функция sorted() создает новый отсортированный список
print("Список после sorted():", sorted_arr)  # Вывод: [1, 2, 5, 5, 6, 9]
print("Исходный список остался без изменений:", arr2)  # Вывод: [5, 2, 9, 1, 5, 6]

# Выводим оба результата для сравнения
print("Результаты:")
print("После sort():", arr)  # Отсортированный по методу sort()
print("После sorted():", sorted_arr)  # Новый список от функции sorted()
